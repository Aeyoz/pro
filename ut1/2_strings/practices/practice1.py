num_a = int(input("Introduce the first number: "))
num_b = int(input("Introduce the second number: "))

result = num_a / num_b
print(num_a, "/", num_b, "=", result)

result = num_a * num_b
print(num_a, "*", num_b, "=", result)

result = num_a + num_b
print(num_a, "+", num_b, "=", result)

result = num_a - num_b
print(num_a, "-", num_b, "=", result)

